    sealed class ExecuteBodyRewriter : CSharpSyntaxRewriter
    {
        readonly SemanticModel semanticModel;
        readonly INamedTypeSymbol systemType;
        readonly Dictionary<string, AccessModel> accessMap;
        readonly bool injectEntityId;
        readonly string entityIndexPlaceholder;

        public string? ErrorMessage { get; private set; }

        public ExecuteBodyRewriter(SemanticModel semanticModel, INamedTypeSymbol systemType, ImmutableArray<AccessModel> accesses, bool injectEntityId, string entityIndexPlaceholder)
        {
            this.semanticModel = semanticModel;
            this.systemType = systemType;
            this.injectEntityId = injectEntityId;
            this.entityIndexPlaceholder = entityIndexPlaceholder;
            accessMap = accesses.ToDictionary(static access => access.ParameterName, System.StringComparer.Ordinal);
        }

        public override SyntaxNode? VisitThisExpression(ThisExpressionSyntax node)
            => SyntaxFactory.IdentifierName("system").WithTriviaFrom(node);

        public override SyntaxNode? VisitIdentifierName(IdentifierNameSyntax node)
        {
            if (node.Identifier.ValueText == "entityId" && injectEntityId)
                return node;

            if (accessMap.ContainsKey(node.Identifier.ValueText))
                return node;

            if (node.Parent is MemberAccessExpressionSyntax memberAccess && memberAccess.Name == node)
                return node;

            if (node.Parent is NameColonSyntax || node.Parent is TypeArgumentListSyntax || node.Parent is QualifiedNameSyntax)
                return node;

            if (semanticModel.GetSymbolInfo(node).Symbol is not ISymbol symbol)
                return node;

            if (symbol.IsStatic || !SymbolEqualityComparer.Default.Equals(symbol.ContainingType, systemType))
                return node;

            if (symbol is IFieldSymbol or IPropertySymbol or IMethodSymbol)
            {
                return SyntaxFactory.MemberAccessExpression(
                        SyntaxKind.SimpleMemberAccessExpression,
                        SyntaxFactory.IdentifierName("system"),
                        SyntaxFactory.IdentifierName(node.Identifier))
                    .WithTriviaFrom(node);
            }

            return node;
        }

        public override SyntaxNode? VisitMemberAccessExpression(MemberAccessExpressionSyntax node)
        {
            if (TryGetValueAccess(node.Expression, out string? parameterName))
            {
                return SyntaxFactory.MemberAccessExpression(
                        SyntaxKind.SimpleMemberAccessExpression,
                        CreateIndexedAccess(parameterName!),
                        (SimpleNameSyntax)Visit(node.Name)!)
                    .WithTriviaFrom(node);
            }

            if (TryGetValueAccess(node, out parameterName))
            {
                return CreateIndexedAccess(parameterName!)
                    .WithTriviaFrom(node);
            }

            return base.VisitMemberAccessExpression(node);
        }

        public override SyntaxNode? VisitElementAccessExpression(ElementAccessExpressionSyntax node)
        {
            if (TryGetValueAccess(node.Expression, out string? parameterName))
            {
                return SyntaxFactory.ElementAccessExpression(
                        CreateIndexedAccess(parameterName!),
                        (BracketedArgumentListSyntax)Visit(node.ArgumentList)!)
                    .WithTriviaFrom(node);
            }

            return base.VisitElementAccessExpression(node);
        }

        public override SyntaxNode? VisitAnonymousMethodExpression(AnonymousMethodExpressionSyntax node)
        {
            ErrorMessage = "anonymous functions are not supported inside Execute.";
            return node;
        }

        public override SyntaxNode? VisitSimpleLambdaExpression(SimpleLambdaExpressionSyntax node)
        {
            ErrorMessage = "lambda expressions are not supported inside Execute.";
            return node;
        }

        public override SyntaxNode? VisitParenthesizedLambdaExpression(ParenthesizedLambdaExpressionSyntax node)
        {
            ErrorMessage = "lambda expressions are not supported inside Execute.";
            return node;
        }

        bool TryGetValueAccess(ExpressionSyntax expression, out string? parameterName)
        {
            parameterName = null;
            if (expression is not MemberAccessExpressionSyntax memberAccess)
                return false;

            if (memberAccess.Name.Identifier.ValueText != "Value")
                return false;

            if (memberAccess.Expression is not IdentifierNameSyntax identifier)
                return false;

            parameterName = identifier.Identifier.ValueText;
            return accessMap.ContainsKey(parameterName);
        }

        ElementAccessExpressionSyntax CreateIndexedAccess(string parameterName)
        {
            return SyntaxFactory.ElementAccessExpression(
                SyntaxFactory.IdentifierName($"__{parameterName}Base"),
                SyntaxFactory.BracketedArgumentList(
                    SyntaxFactory.SingletonSeparatedList(
                        SyntaxFactory.Argument(SyntaxFactory.IdentifierName(entityIndexPlaceholder)))));
        }
    }

