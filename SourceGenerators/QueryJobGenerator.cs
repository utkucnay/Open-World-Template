    {
        public readonly string SystemName;
        public readonly string SystemFullyQualifiedName;
        public readonly string? Namespace;
        public readonly ImmutableArray<string> Usings;
        public readonly ImmutableArray<AccessModel> Accesses;
        public readonly ExecuteMethodModel? ScalarExecute;
        public readonly ExecuteMethodModel? SseExecute;
        public readonly ExecuteMethodModel? AvxExecute;
        public readonly QueryExecutionMode ExecutionMode;
        public readonly bool IsNonBurst;
        public readonly bool InjectEntityId;

        public SystemModel(string systemName, string systemFullyQualifiedName, string? ns, ImmutableArray<string> usings, ImmutableArray<AccessModel> accesses, ExecuteMethodModel? scalarExecute, ExecuteMethodModel? sseExecute, ExecuteMethodModel? avxExecute, QueryExecutionMode executionMode, bool isNonBurst, bool injectEntityId)
        {
            SystemName = systemName;
            SystemFullyQualifiedName = systemFullyQualifiedName;
            Namespace = ns;
            Usings = usings;
            Accesses = accesses;
            ScalarExecute = scalarExecute;
            SseExecute = sseExecute;
            AvxExecute = avxExecute;
            ExecutionMode = executionMode;
            IsNonBurst = isNonBurst;
            InjectEntityId = injectEntityId;
        }

        public string ExecutionModeName => $"QueryExecution.{ExecutionMode}";

        public bool Equals(SystemModel other)
        {
            if (SystemFullyQualifiedName != other.SystemFullyQualifiedName) return false;
            if (Namespace != other.Namespace) return false;
            if (!NullableEquals(ScalarExecute, other.ScalarExecute)) return false;
            if (!NullableEquals(SseExecute, other.SseExecute)) return false;
            if (!NullableEquals(AvxExecute, other.AvxExecute)) return false;
            if (ExecutionMode != other.ExecutionMode) return false;
            if (IsNonBurst != other.IsNonBurst) return false;
            if (InjectEntityId != other.InjectEntityId) return false;
            if (Usings.Length != other.Usings.Length) return false;
            for (int i = 0; i < Usings.Length; i++)
            {
                if (Usings[i] != other.Usings[i]) return false;
            }
            if (Accesses.Length != other.Accesses.Length) return false;
            for (int i = 0; i < Accesses.Length; i++)
            {
                if (!Accesses[i].Equals(other.Accesses[i])) return false;
            }
            return true;
        }

        public override int GetHashCode()
        {
            int hash = SystemFullyQualifiedName?.GetHashCode() ?? 0;
            hash = hash * 31 + (ScalarExecute?.GetHashCode() ?? 0);
            hash = hash * 31 + (SseExecute?.GetHashCode() ?? 0);
            hash = hash * 31 + (AvxExecute?.GetHashCode() ?? 0);
            hash = hash * 31 + (int)ExecutionMode;
            hash = hash * 31 + (IsNonBurst ? 1 : 0);
            hash = hash * 31 + (InjectEntityId ? 1 : 0);
            foreach (string usingDirective in Usings)
                hash = hash * 31 + (usingDirective?.GetHashCode() ?? 0);
            foreach (AccessModel access in Accesses)
                hash = hash * 31 + access.GetHashCode();
            return hash;
        }

        static bool NullableEquals(ExecuteMethodModel? left, ExecuteMethodModel? right)
        {
            if (left.HasValue != right.HasValue)
                return false;

            return !left.HasValue || left.Value.Equals(right!.Value);
        }
