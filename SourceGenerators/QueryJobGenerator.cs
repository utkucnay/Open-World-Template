    readonly struct AccessModel : System.IEquatable<AccessModel>
    {
        public readonly string ParameterName;
        public readonly AccessMode Mode;
        public readonly bool IsBuffer;
        public readonly int BufferCapacity;
        public readonly string BufferWrapperName;
        public readonly string AccessFullName;
        public readonly string InnerFullName;

        public AccessModel(string parameterName, AccessMode mode, bool isBuffer, int bufferCapacity, string bufferWrapperName, string accessFullName, string innerFullName)
        {
            ParameterName = parameterName;
            Mode = mode;
            IsBuffer = isBuffer;
            BufferCapacity = bufferCapacity;
            BufferWrapperName = bufferWrapperName;
            AccessFullName = accessFullName;
            InnerFullName = innerFullName;
        }

        public bool Equals(AccessModel other)
        {
            return ParameterName == other.ParameterName
                && Mode == other.Mode
                && IsBuffer == other.IsBuffer
                && BufferCapacity == other.BufferCapacity
                && BufferWrapperName == other.BufferWrapperName
                && AccessFullName == other.AccessFullName
                && InnerFullName == other.InnerFullName;
        }

        public override int GetHashCode()
        {
            int hash = ParameterName?.GetHashCode() ?? 0;
            hash = hash * 31 + (int)Mode;
            hash = hash * 31 + (IsBuffer ? 1 : 0);
            hash = hash * 31 + BufferCapacity;
            hash = hash * 31 + (BufferWrapperName?.GetHashCode() ?? 0);
            hash = hash * 31 + (AccessFullName?.GetHashCode() ?? 0);
            hash = hash * 31 + (InnerFullName?.GetHashCode() ?? 0);
            return hash;
        }
    }

    readonly struct ExecuteMethodModel : System.IEquatable<ExecuteMethodModel>
    {
        public readonly ExecuteVariant Variant;
        public readonly ImmutableArray<AccessModel> Accesses;
        public readonly bool InjectEntityId;
        public readonly string RewrittenBody;

        public ExecuteMethodModel(ExecuteVariant variant, ImmutableArray<AccessModel> accesses, bool injectEntityId, string rewrittenBody)
        {
            Variant = variant;
            Accesses = accesses;
            InjectEntityId = injectEntityId;
            RewrittenBody = rewrittenBody;
        }

        public bool Equals(ExecuteMethodModel other)
        {
            if (Variant != other.Variant) return false;
            if (InjectEntityId != other.InjectEntityId) return false;
            if (RewrittenBody != other.RewrittenBody) return false;
            if (Accesses.Length != other.Accesses.Length) return false;
            for (int i = 0; i < Accesses.Length; i++)
            {
                if (!Accesses[i].Equals(other.Accesses[i])) return false;
            }
            return true;
        }

        public override int GetHashCode()
        {
            int hash = (int)Variant;
            hash = hash * 31 + (InjectEntityId ? 1 : 0);
            hash = hash * 31 + (RewrittenBody?.GetHashCode() ?? 0);
            foreach (AccessModel access in Accesses)
                hash = hash * 31 + access.GetHashCode();
            return hash;
        }
    }

