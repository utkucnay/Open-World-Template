        static bool TryResolveExecuteMethods(SemanticModel semanticModel, StructDeclarationSyntax structSyntax, INamedTypeSymbol namedType, out ExecuteSetModel executeModel, out DiagnosticInfo? diagnostic)
        {
            executeModel = default;
            diagnostic = null;

            ExecuteMethodModel? scalarExecute = null;
            ExecuteMethodModel? sseExecute = null;
            ExecuteMethodModel? avxExecute = null;
            foreach (MethodDeclarationSyntax methodSyntax in structSyntax.Members.OfType<MethodDeclarationSyntax>())
            {
                ExecuteVariant variant;
                switch (methodSyntax.Identifier.ValueText)
                {
                    case "Execute":
                        variant = ExecuteVariant.Scalar;
                        break;
                    case "ExecuteSSE":
                        variant = ExecuteVariant.Sse;
                        break;
                    case "ExecuteAVX":
                        variant = ExecuteVariant.Avx;
                        break;
                    default:
                        continue;
                }

                if ((variant == ExecuteVariant.Scalar && scalarExecute.HasValue)
                    || (variant == ExecuteVariant.Sse && sseExecute.HasValue)
                    || (variant == ExecuteVariant.Avx && avxExecute.HasValue))
                {
                    diagnostic = new DiagnosticInfo(
                        AmbiguousExecuteSignature,
                        namedType.Locations.FirstOrDefault(),
                        namedType.Name);
                    return false;
                }

                if (!TryCreateExecuteMethodModel(semanticModel, methodSyntax, namedType, variant, out ExecuteMethodModel methodModel, out diagnostic))
                    return false;

                switch (variant)
                {
                    case ExecuteVariant.Scalar:
                        scalarExecute = methodModel;
                        break;
                    case ExecuteVariant.Sse:
                        sseExecute = methodModel;
                        break;
                    case ExecuteVariant.Avx:
                        avxExecute = methodModel;
                        break;
                }
            }

            if (!scalarExecute.HasValue && !sseExecute.HasValue && !avxExecute.HasValue)
            {
                diagnostic = new DiagnosticInfo(
                    InvalidExecuteSignature,
                    namedType.Locations.FirstOrDefault(),
                    namedType.Name,
                    "Execute(int entityId?, RefRW<T>/RefR<T>/BufferR<T>/BufferRW<T>...), ExecuteSSE(EntityId4?, RefRW4<T>/RefR4<T>/BufferR4<T>/BufferRW4<T>...), or ExecuteAVX(EntityId8?, RefRW8<T>/RefR8<T>/BufferR8<T>/BufferRW8<T>...) with 1 to 8 unique component parameters");
                return false;
            }

            ImmutableArray<AccessModel> accesses = avxExecute?.Accesses ?? sseExecute?.Accesses ?? scalarExecute!.Value.Accesses;
            if ((scalarExecute.HasValue && !HasSameAccessSignature(accesses, scalarExecute.Value.Accesses))
                || (sseExecute.HasValue && !HasSameAccessSignature(accesses, sseExecute.Value.Accesses))
                || (avxExecute.HasValue && !HasSameAccessSignature(accesses, avxExecute.Value.Accesses)))
            {
                diagnostic = new DiagnosticInfo(
                    UnsupportedExecuteSyntax,
                    namedType.Locations.FirstOrDefault(),
                    namedType.Name,
                    "Execute, ExecuteSSE, and ExecuteAVX must use the same parameter names, component types, order, and read/write access modes.");
                return false;
            }

            bool injectEntityId = (scalarExecute?.InjectEntityId ?? false)
                || (sseExecute?.InjectEntityId ?? false)
                || (avxExecute?.InjectEntityId ?? false);
            executeModel = new ExecuteSetModel(accesses, scalarExecute, sseExecute, avxExecute, injectEntityId);
            return true;
        }

        static bool TryCreateExecuteMethodModel(SemanticModel semanticModel, MethodDeclarationSyntax methodSyntax, INamedTypeSymbol namedType, ExecuteVariant variant, out ExecuteMethodModel executeModel, out DiagnosticInfo? diagnostic)
        {
            executeModel = default;
            diagnostic = null;

            if (semanticModel.GetDeclaredSymbol(methodSyntax) is not IMethodSymbol methodSymbol)
                return false;

            if (methodSymbol.MethodKind != MethodKind.Ordinary || methodSymbol.IsStatic || !methodSymbol.ReturnsVoid)
            {
                diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, methodSyntax.GetLocation(), namedType.Name, $"{methodSyntax.Identifier.ValueText} must be a non-static void method.");
                return false;
            }

            bool injectEntityId = false;
            int offset = 0;
            if (variant == ExecuteVariant.Scalar && methodSymbol.Parameters.Length > 0 && methodSymbol.Parameters[0].Type.SpecialType == SpecialType.System_Int32)
            {
                injectEntityId = true;
                offset = 1;
            }
            else if (variant == ExecuteVariant.Sse && methodSymbol.Parameters.Length > 0 && IsEntityId4(methodSymbol.Parameters[0].Type))
            {
                injectEntityId = true;
                offset = 1;
            }
            else if (variant == ExecuteVariant.Avx && methodSymbol.Parameters.Length > 0 && IsEntityId8(methodSymbol.Parameters[0].Type))
            {
                injectEntityId = true;
                offset = 1;
            }

            if (variant != ExecuteVariant.Scalar && methodSymbol.Parameters.Length > 0 && methodSymbol.Parameters[0].Type.SpecialType == SpecialType.System_Int32)
            {
                diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, methodSyntax.GetLocation(), namedType.Name, $"{methodSyntax.Identifier.ValueText} does not support entityId parameters.");
                return false;
            }
            if (variant == ExecuteVariant.Sse && methodSymbol.Parameters.Length > 0 && IsEntityId8(methodSymbol.Parameters[0].Type))
            {
                diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, methodSyntax.GetLocation(), namedType.Name, "ExecuteSSE expects EntityId4 parameters.");
                return false;
            }
            if (variant == ExecuteVariant.Avx && methodSymbol.Parameters.Length > 0 && IsEntityId4(methodSymbol.Parameters[0].Type))
            {
                diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, methodSyntax.GetLocation(), namedType.Name, "ExecuteAVX expects EntityId8 parameters.");
                return false;
            }

            int bindableParameterCount = methodSymbol.Parameters.Length - offset;
            if (bindableParameterCount <= 0 || bindableParameterCount > 8)
            {
                diagnostic = new DiagnosticInfo(InvalidExecuteSignature, methodSyntax.GetLocation(), namedType.Name, GetExecuteSignatureDescription(variant));
                return false;
            }

            var accesses = ImmutableArray.CreateBuilder<AccessModel>(bindableParameterCount);
            var seenComponentTypes = new HashSet<string>(System.StringComparer.Ordinal);
            for (int i = offset; i < methodSymbol.Parameters.Length; i++)
            {
                IParameterSymbol parameter = methodSymbol.Parameters[i];
                if (!TryCreateAccessModel(parameter, variant, out AccessModel access, out DiagnosticInfo? accessDiagnostic))
                {
                    diagnostic = accessDiagnostic;
                    return false;
                }

                if (!seenComponentTypes.Add(access.InnerFullName))
                {
                    diagnostic = new DiagnosticInfo(DuplicateComponentAccess, parameter.Locations.FirstOrDefault(), namedType.Name, access.InnerFullName);
                    return false;
                }

                accesses.Add(access);
            }

            string rewrittenBody = string.Empty;
            if (variant == ExecuteVariant.Scalar)
            {
                if (methodSyntax.ExpressionBody != null || methodSyntax.Body == null)
                {
                    diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, methodSyntax.GetLocation(), namedType.Name, "Execute must use a block body.");
                    return false;
                }

                var rewriter = new ExecuteBodyRewriter(semanticModel, namedType, accesses.ToImmutable(), injectEntityId, "__ENTITY_INDEX__");
                BlockSyntax rewrittenSyntax = (BlockSyntax)rewriter.Visit(methodSyntax.Body)!;
                if (rewriter.ErrorMessage != null)
                {
                    diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, methodSyntax.GetLocation(), namedType.Name, rewriter.ErrorMessage);
                    return false;
                }

                rewrittenBody = string.Concat(rewrittenSyntax.Statements.Select(static statement => statement.NormalizeWhitespace().ToFullString() + "\n")).TrimEnd();
            }

            executeModel = new ExecuteMethodModel(variant, accesses.ToImmutable(), injectEntityId, rewrittenBody);
            return true;
        }

        static string GetExecuteSignatureDescription(ExecuteVariant variant)
        {
            return variant switch
            {
                ExecuteVariant.Scalar => "Execute(int entityId?, RefRW<T>/RefR<T>...) with 1 to 8 unique component parameters",
                ExecuteVariant.Sse => "ExecuteSSE(EntityId4?, RefRW4<T>/RefR4<T>/BufferR4<T>/BufferRW4<T>...) with 1 to 8 unique component parameters",
                ExecuteVariant.Avx => "ExecuteAVX(EntityId8?, RefRW8<T>/RefR8<T>/BufferR8<T>/BufferRW8<T>...) with 1 to 8 unique component parameters",
