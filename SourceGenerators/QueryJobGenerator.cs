                sb.AppendLine($"{indent}}}");

                if (group.Key != null)
                    sb.AppendLine("}");

                string hintName = (group.Key ?? "Global").Replace('.', '_') + "__ECSAPI.g.cs";
                spc.AddSource(hintName, sb.ToString());
            }
        }

        static void EmitAccessLocals(StringBuilder sb, SystemModel model, string indent, string dataExpr, bool includeEntityIdsLocal)
        {
            for (int i = 0; i < model.Accesses.Length; i++)
            {
                if (model.Accesses[i].Kind == AccessKind.Ref)
                {
                    sb.AppendLine($"{indent}        {model.Accesses[i].InnerFullName}* p{i} = ({model.Accesses[i].InnerFullName}*){dataExpr}.c{i};");
                }
                else
                {
                    sb.AppendLine($"{indent}        byte* p{i} = {dataExpr}.c{i};");
                    sb.AppendLine($"{indent}        int s{i} = {dataExpr}.s{i};");
                }
            }

            if (model.InjectEntityId && includeEntityIdsLocal)
                sb.AppendLine($"{indent}        int* entityIds = {dataExpr}.entityIds;");
        }

        static bool TryResolveExecuteSignature(INamedTypeSymbol namedType, out ImmutableArray<AccessModel> accesses, out bool injectEntityId, out DiagnosticInfo? diagnostic)
        {
            accesses = default;
            injectEntityId = false;
            diagnostic = null;

            var validMethods = ImmutableArray.CreateBuilder<ExecuteSignatureModel>();
            var executeMethods = namedType.GetMembers("Execute").OfType<IMethodSymbol>();
            foreach (IMethodSymbol method in executeMethods)
            {
                if (method.MethodKind != MethodKind.Ordinary || method.IsStatic || !method.ReturnsVoid)
                    continue;

                if (TryCreateExecuteSignature(method, out ExecuteSignatureModel signature))
                    validMethods.Add(signature);
            }

            if (validMethods.Count == 0)
            {
                diagnostic = new DiagnosticInfo(
                    InvalidExecuteSignature,
                    namedType.Locations.FirstOrDefault(),
                    namedType.Name,
                    "Execute(Ref<T>/Buffer<T>...) or Execute(int entityId, Ref<T>/Buffer<T>...) with 1 to 7 bindable parameters");
                return false;
            }

            if (validMethods.Count > 1)
            {
                diagnostic = new DiagnosticInfo(
                    AmbiguousExecuteSignature,
                    namedType.Locations.FirstOrDefault(),
                    namedType.Name);
                return false;
            }

            accesses = validMethods[0].Accesses;
            injectEntityId = validMethods[0].InjectEntityId;
            return true;
        }

        static bool TryCreateExecuteSignature(IMethodSymbol method, out ExecuteSignatureModel signature)
        {
            signature = default;
            bool injectEntityId = false;
            int offset = 0;

            if (method.Parameters.Length == 0)
                return false;

            if (method.Parameters[0].Type.SpecialType == SpecialType.System_Int32)
            {
                injectEntityId = true;
                offset = 1;
            }

            int bindableParameterCount = method.Parameters.Length - offset;
            if (bindableParameterCount <= 0 || bindableParameterCount > 7)
                return false;

            var accesses = ImmutableArray.CreateBuilder<AccessModel>(bindableParameterCount);
            for (int i = offset; i < method.Parameters.Length; i++)
            {
                if (!TryCreateAccessModel(method.Parameters[i].Type, out AccessModel access))
                    return false;

                accesses.Add(access);
            }

            signature = new ExecuteSignatureModel(accesses.ToImmutable(), injectEntityId);
            return true;
        }

        static bool TryCreateAccessModel(ITypeSymbol parameterType, out AccessModel access)
        {
            access = default;
            if (parameterType is not INamedTypeSymbol accessType || accessType.TypeArguments.Length != 1)
                return false;

            var ns = accessType.OriginalDefinition.ContainingNamespace?.ToDisplayString();
            var metadataName = accessType.OriginalDefinition.MetadataName;
            AccessKind kind;
            if (ns == "Glai.ECS" && metadataName == "Ref`1")
            {
                kind = AccessKind.Ref;
            }
            else if (ns == "Glai.ECS" && metadataName == "Buffer`1")
            {
                kind = AccessKind.Buffer;
            }
            else
            {
                return false;
            }

            access = new AccessModel(
                kind,
                accessType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
                accessType.TypeArguments[0].ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat));
            return true;
        }

        static string BuildExecuteArgs(ImmutableArray<AccessModel> accesses, string indexExpr, bool injectEntityId, string entityIdExpr)
        {
            var args = new StringBuilder();
            if (injectEntityId)
                args.Append(entityIdExpr);

            for (int i = 0; i < accesses.Length; i++)
            {
                if (args.Length > 0)
                    args.Append(", ");

                var access = accesses[i];
                if (access.Kind == AccessKind.Ref)
                {
                    args.Append($"new global::Glai.ECS.Ref<{access.InnerFullName}>(&p{i}[{indexExpr}])");
                }
                else
                {
                    args.Append($"new global::Glai.ECS.Buffer<{access.InnerFullName}>(p{i} + ({indexExpr}) * s{i}, global::Glai.ECS.Core.BufferTypeInfo<{access.InnerFullName}>.GetCapacity(s{i}))");
                }
            }

            return args.ToString();
        }

        static bool TryGetQueryExecution(INamedTypeSymbol namedType, out QueryExecutionMode executionMode, out DiagnosticInfo? diagnostic)
        {
            executionMode = default;
            diagnostic = null;

            foreach (AttributeData attribute in namedType.GetAttributes())
            {
                if (attribute.AttributeClass?.ContainingNamespace?.ToDisplayString() != "Glai.ECS")
                    continue;

                if (attribute.AttributeClass.MetadataName != "QueryJobAttribute")
                    continue;

                if (attribute.ConstructorArguments.Length != 1)
                {
                    diagnostic = new DiagnosticInfo(MissingExecutionMode, namedType.Locations.FirstOrDefault(), namedType.Name);
                    return false;
                }

                TypedConstant arg = attribute.ConstructorArguments[0];
                if (arg.Value is not int rawValue || !System.Enum.IsDefined(typeof(QueryExecutionMode), rawValue))
                {
                    diagnostic = new DiagnosticInfo(InvalidExecutionMode, namedType.Locations.FirstOrDefault(), namedType.Name);
                    return false;
                }

                executionMode = (QueryExecutionMode)rawValue;
                return true;
            }

            return false;
        }

        static bool HasNonBurstQueryAttribute(INamedTypeSymbol namedType)
        {
            foreach (AttributeData attribute in namedType.GetAttributes())
            {
                if (attribute.AttributeClass?.ContainingNamespace?.ToDisplayString() != "Glai.ECS")
                    continue;

                if (attribute.AttributeClass.MetadataName == "NonBurstQueryAttribute")
                    return true;
            }

            return false;
        }

