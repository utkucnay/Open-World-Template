    enum QueryExecutionMode
    {
        MainThread,
        Async,
        ChunkParallel,
        EntityParallel,
    }

    enum AccessKind
    {
        Ref,
        Buffer,
    }

    readonly struct AccessModel : System.IEquatable<AccessModel>
    {
        public readonly AccessKind Kind;
        public readonly string AccessFullName;
        public readonly string InnerFullName;

        public AccessModel(AccessKind kind, string accessFullName, string innerFullName)
        {
            Kind = kind;
            AccessFullName = accessFullName;
            InnerFullName = innerFullName;
        }

        public bool Equals(AccessModel other)
        {
            return Kind == other.Kind
                && AccessFullName == other.AccessFullName
                && InnerFullName == other.InnerFullName;
        }

        public override int GetHashCode()
        {
            int hash = (int)Kind;
            hash = hash * 31 + (AccessFullName?.GetHashCode() ?? 0);
            hash = hash * 31 + (InnerFullName?.GetHashCode() ?? 0);
            return hash;
        }
    }

    readonly struct SystemModel : System.IEquatable<SystemModel>
    {
        public readonly string SystemName;
        public readonly string SystemFullyQualifiedName;
        public readonly string? Namespace;
        public readonly ImmutableArray<AccessModel> Accesses;
        public readonly QueryExecutionMode ExecutionMode;
        public readonly bool IsNonBurst;
        public readonly bool InjectEntityId;

        public SystemModel(string systemName, string systemFullyQualifiedName, string? ns, ImmutableArray<AccessModel> accesses, QueryExecutionMode executionMode, bool isNonBurst, bool injectEntityId)
        {
            SystemName = systemName;
            SystemFullyQualifiedName = systemFullyQualifiedName;
            Namespace = ns;
            Accesses = accesses;
            ExecutionMode = executionMode;
            IsNonBurst = isNonBurst;
            InjectEntityId = injectEntityId;
        }

        public string ExecutionModeName => $"QueryExecution.{ExecutionMode}";

        public bool Equals(SystemModel other)
        {
            if (SystemFullyQualifiedName != other.SystemFullyQualifiedName) return false;
            if (Namespace != other.Namespace) return false;
            if (ExecutionMode != other.ExecutionMode) return false;
            if (IsNonBurst != other.IsNonBurst) return false;
            if (InjectEntityId != other.InjectEntityId) return false;
            if (Accesses.Length != other.Accesses.Length) return false;
            for (int i = 0; i < Accesses.Length; i++)
            {
                if (!Accesses[i].Equals(other.Accesses[i])) return false;
            }
            return true;
        }

        public override int GetHashCode()
        {
            int hash = SystemFullyQualifiedName?.GetHashCode() ?? 0;
            hash = hash * 31 + (int)ExecutionMode;
            hash = hash * 31 + (IsNonBurst ? 1 : 0);
            hash = hash * 31 + (InjectEntityId ? 1 : 0);
            foreach (var access in Accesses)
            {
                hash = hash * 31 + access.GetHashCode();
            }
            return hash;
        }
    }

