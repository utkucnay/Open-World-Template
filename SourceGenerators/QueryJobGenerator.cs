        static bool IsEntityId4(ITypeSymbol type)
        {
            if (type is not INamedTypeSymbol namedType)
                return false;

            return namedType.ContainingNamespace?.ToDisplayString() == "Glai.ECS"
                && namedType.MetadataName == "EntityId4";
        }

        static bool IsEntityId8(ITypeSymbol type)
        {
            if (type is not INamedTypeSymbol namedType)
                return false;

            return namedType.ContainingNamespace?.ToDisplayString() == "Glai.ECS"
                && namedType.MetadataName == "EntityId8";
        }

        static bool HasSameAccessSignature(ImmutableArray<AccessModel> left, ImmutableArray<AccessModel> right)
        {
            if (left.Length != right.Length)
                return false;

            for (int i = 0; i < left.Length; i++)
            {
                if (left[i].ParameterName != right[i].ParameterName || left[i].Mode != right[i].Mode || left[i].IsBuffer != right[i].IsBuffer || left[i].BufferCapacity != right[i].BufferCapacity || left[i].BufferWrapperName != right[i].BufferWrapperName || left[i].InnerFullName != right[i].InnerFullName)
                    return false;
            }

            return true;
        }

        static bool TryCreateAccessModel(IParameterSymbol parameter, ExecuteVariant variant, out AccessModel access, out DiagnosticInfo? diagnostic)
        {
            access = default;
            diagnostic = null;

            if (parameter.Type is not INamedTypeSymbol accessType || accessType.TypeArguments.Length != 1)
            {
                diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, parameter.Locations.FirstOrDefault(), parameter.ContainingType?.Name ?? "QueryJob", $"Unsupported parameter '{parameter.Name}'. Use {GetAccessTypeDescription(variant)}.");
                return false;
            }

            string? ns = accessType.OriginalDefinition.ContainingNamespace?.ToDisplayString();
            string metadataName = accessType.OriginalDefinition.MetadataName;
            AccessMode mode;
            bool isBuffer = false;
            int bufferCapacity = 0;
            string bufferWrapperName = string.Empty;
            if (ns == "Glai.ECS" && metadataName == GetReadOnlyAccessMetadataName(variant))
            {
                mode = AccessMode.ReadOnly;
            }
            else if (ns == "Glai.ECS" && metadataName == GetReadWriteAccessMetadataName(variant))
            {
                mode = AccessMode.ReadWrite;
            }
            else if (ns == "Glai.ECS" && TryGetBufferAccess(variant, metadataName, out mode, out bufferWrapperName))
            {
                isBuffer = true;
                if (!TryGetFixedBufferCapacity(accessType.TypeArguments[0], out bufferCapacity))
                {
                    diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, parameter.Locations.FirstOrDefault(), parameter.ContainingType?.Name ?? "QueryJob", $"Buffer parameter '{parameter.Name}' component type must have [FixedBuffer(capacity)].");
                    return false;
                }
            }
            else
            {
                diagnostic = new DiagnosticInfo(UnsupportedExecuteSyntax, parameter.Locations.FirstOrDefault(), parameter.ContainingType?.Name ?? "QueryJob", $"Parameter '{parameter.Name}' must use {GetAccessTypeDescription(variant)}.");
                return false;
            }

            access = new AccessModel(
                parameter.Name,
                mode,
                isBuffer,
                bufferCapacity,
                bufferWrapperName,
                accessType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
                accessType.TypeArguments[0].ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat));
            return true;
        }

        static bool TryGetFixedBufferCapacity(ITypeSymbol bufferElementType, out int capacity)
        {
            foreach (AttributeData attribute in bufferElementType.GetAttributes())
            {
                if (attribute.AttributeClass?.ContainingNamespace?.ToDisplayString() != "Glai.ECS")
                    continue;

                if (attribute.AttributeClass.MetadataName != "FixedBufferAttribute")
                    continue;

                if (attribute.ConstructorArguments.Length == 1 && attribute.ConstructorArguments[0].Value is int value && value > 0)
                {
                    capacity = value;
                    return true;
                }
            }

            capacity = 0;
            return false;
        }

