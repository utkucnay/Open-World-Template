        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var systemResults = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (node, _) => node is StructDeclarationSyntax,
                    transform: static (ctx, ct) => ExtractSystemModel(ctx, ct));

            var systemModels = systemResults
                .Where(static r => r.Model != null)
                .Select(static (r, _) => r.Model!.Value);

            var allSystemModels = systemModels.Collect();

            context.RegisterSourceOutput(systemResults, static (spc, result) => Emit(spc, result));
            context.RegisterSourceOutput(allSystemModels, static (spc, models) => EmitNamespaceApis(spc, models));
        }

        static SystemModelResult ExtractSystemModel(GeneratorSyntaxContext ctx, CancellationToken ct)
        {
            var structSyntax = (StructDeclarationSyntax)ctx.Node;
            var symbol = ctx.SemanticModel.GetDeclaredSymbol(structSyntax, ct);
            if (symbol is not INamedTypeSymbol namedType)
                return default;

            if (namedType.ContainingType != null)
                return default;

            if (!TryGetQueryExecution(namedType, out QueryExecutionMode executionMode, out DiagnosticInfo? queryDiagnostic))
                return new SystemModelResult(null, queryDiagnostic);

            if (!TryResolveExecuteSignature(namedType, out var accesses, out bool injectEntityId, out DiagnosticInfo? executeDiagnostic))
                return new SystemModelResult(null, executeDiagnostic);

            var namespaceSymbol = namedType.ContainingNamespace;
            string? namespaceName = (namespaceSymbol != null && !namespaceSymbol.IsGlobalNamespace)
                ? namespaceSymbol.ToDisplayString()
                : null;

            return new SystemModelResult(new SystemModel(
                namedType.Name,
                namedType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
                namespaceName,
                accesses,
                executionMode,
                HasNonBurstQueryAttribute(namedType),
                injectEntityId), null);
        }

        static void Emit(SourceProductionContext spc, SystemModelResult result)
        {
            if (result.Diagnostic is DiagnosticInfo diagnostic)
                spc.ReportDiagnostic(Diagnostic.Create(diagnostic.Descriptor, diagnostic.Location, diagnostic.MessageArgs));

            if (result.Model is SystemModel model)
                EmitModel(spc, model);
        }

