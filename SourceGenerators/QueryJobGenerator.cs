        static bool TryGetBufferAccess(ExecuteVariant variant, string metadataName, out AccessMode mode, out string wrapperName)
        {
            mode = AccessMode.ReadWrite;
            wrapperName = string.Empty;

            switch (variant)
            {
                case ExecuteVariant.Scalar:
                    if (metadataName == "BufferR`1")
                    {
                        mode = AccessMode.ReadOnly;
                        wrapperName = "BufferR";
                        return true;
                    }
                    if (metadataName == "BufferRW`1")
                    {
                        wrapperName = "BufferRW";
                        return true;
                    }
                    return false;
                case ExecuteVariant.Sse:
                    if (metadataName == "BufferR4`1")
                    {
                        mode = AccessMode.ReadOnly;
                        wrapperName = "BufferR4";
                        return true;
                    }
                    if (metadataName == "BufferRW4`1")
                    {
                        wrapperName = "BufferRW4";
                        return true;
                    }
                    return false;
                case ExecuteVariant.Avx:
                    if (metadataName == "BufferR8`1")
                    {
                        mode = AccessMode.ReadOnly;
                        wrapperName = "BufferR8";
                        return true;
                    }
                    if (metadataName == "BufferRW8`1")
                    {
                        wrapperName = "BufferRW8";
                        return true;
                    }
                    return false;
                default:
                    return false;
            }
        }

        static string GetAccessTypeDescription(ExecuteVariant variant)
        {
            return variant switch
            {
                ExecuteVariant.Scalar => "RefR<T>, RefRW<T>, BufferR<T>, or BufferRW<T>",
                ExecuteVariant.Sse => "RefR4<T>, RefRW4<T>, BufferR4<T>, or BufferRW4<T>",
                ExecuteVariant.Avx => "RefR8<T>, RefRW8<T>, BufferR8<T>, or BufferRW8<T>",
                _ => "a supported query ref type",
            };
        }

        static bool TryGetQueryExecution(INamedTypeSymbol namedType, out QueryExecutionMode executionMode, out DiagnosticInfo? diagnostic)
        {
            executionMode = default;
            diagnostic = null;

            foreach (AttributeData attribute in namedType.GetAttributes())
            {
                if (attribute.AttributeClass?.ContainingNamespace?.ToDisplayString() != "Glai.ECS")
                    continue;

                if (attribute.AttributeClass.MetadataName != "QueryJobAttribute")
                    continue;

                if (attribute.ConstructorArguments.Length != 1)
                {
                    diagnostic = new DiagnosticInfo(MissingExecutionMode, namedType.Locations.FirstOrDefault(), namedType.Name);
                    return false;
                }

                TypedConstant arg = attribute.ConstructorArguments[0];
                if (arg.Value is not int rawValue || !System.Enum.IsDefined(typeof(QueryExecutionMode), rawValue))
                {
                    diagnostic = new DiagnosticInfo(InvalidExecutionMode, namedType.Locations.FirstOrDefault(), namedType.Name);
                    return false;
                }

                executionMode = (QueryExecutionMode)rawValue;
                return true;
            }

            return false;
        }

        static bool HasNonBurstQueryAttribute(INamedTypeSymbol namedType)
        {
            foreach (AttributeData attribute in namedType.GetAttributes())
            {
                if (attribute.AttributeClass?.ContainingNamespace?.ToDisplayString() != "Glai.ECS")
                    continue;

                if (attribute.AttributeClass.MetadataName == "NonBurstQueryAttribute")
                    return true;
            }

            return false;
        }

