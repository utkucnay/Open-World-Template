    [Generator]
    public sealed class SystemGenerator : IIncrementalGenerator
    {
        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var systemResults = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (node, _) => node is StructDeclarationSyntax,
                    transform: static (ctx, ct) => ExtractSystemModel(ctx, ct));

            var systemModels = systemResults
                .Where(static r => r.Model != null)
                .Select(static (r, _) => r.Model!.Value);

            var allSystemModels = systemModels.Collect();

            context.RegisterSourceOutput(systemResults, static (spc, result) => Emit(spc, result));
            context.RegisterSourceOutput(allSystemModels, static (spc, models) => EmitNamespaceApis(spc, models));
        }

        static SystemModelResult ExtractSystemModel(GeneratorSyntaxContext ctx, CancellationToken ct)
        {
            var structSyntax = (StructDeclarationSyntax)ctx.Node;
            var symbol = ctx.SemanticModel.GetDeclaredSymbol(structSyntax, ct);
            if (symbol is not INamedTypeSymbol namedType)
                return default;

            if (namedType.ContainingType != null)
                return default;

            if (!TryGetQueryExecution(namedType, out QueryExecutionMode executionMode, out DiagnosticInfo? queryDiagnostic))
                return new SystemModelResult(null, queryDiagnostic);

            if (!TryResolveExecuteMethods(ctx.SemanticModel, structSyntax, namedType, out ExecuteSetModel executeModel, out DiagnosticInfo? executeDiagnostic))
                return new SystemModelResult(null, executeDiagnostic);

            string? namespaceName = namedType.ContainingNamespace is { IsGlobalNamespace: false } namespaceSymbol
                ? namespaceSymbol.ToDisplayString()
                : null;

            return new SystemModelResult(new SystemModel(
                namedType.Name,
                namedType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
                namespaceName,
                CollectUsings(structSyntax),
                executeModel.Accesses,
                executeModel.ScalarExecute,
                executeModel.SseExecute,
                executeModel.AvxExecute,
                executionMode,
                HasNonBurstQueryAttribute(namedType),
                executeModel.InjectEntityId), null);
        }

        static ImmutableArray<string> CollectUsings(StructDeclarationSyntax structSyntax)
        {
            if (structSyntax.SyntaxTree.GetRoot() is not CompilationUnitSyntax compilationUnit)
                return ImmutableArray<string>.Empty;

            var builder = ImmutableArray.CreateBuilder<string>();
            foreach (UsingDirectiveSyntax usingDirective in compilationUnit.Usings)
            {
                string text = usingDirective.ToString().Trim();
                if (text.Length == 0)
                    continue;

                builder.Add(text.EndsWith(";", System.StringComparison.Ordinal) ? text : text + ";");
            }

            return builder.ToImmutable();
        }

        static void Emit(SourceProductionContext spc, SystemModelResult result)
        {
            if (result.Diagnostic is DiagnosticInfo diagnostic)
                spc.ReportDiagnostic(Diagnostic.Create(diagnostic.Descriptor, diagnostic.Location, diagnostic.MessageArgs));

            if (result.Model is SystemModel model)
                EmitModel(spc, model);
        }

        static void EmitModel(SourceProductionContext spc, SystemModel model)
        {
            var sb = new StringBuilder(8192);
            string indent = model.Namespace != null ? "    " : string.Empty;

            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#pragma warning disable");
            sb.AppendLine("using System.Runtime.CompilerServices;");
            sb.AppendLine("using System.Runtime.InteropServices;");
            sb.AppendLine("using Unity.Burst;");
            sb.AppendLine("using Unity.Collections;");
            sb.AppendLine("using Unity.Collections.LowLevel.Unsafe;");
            sb.AppendLine("using Unity.Jobs;");
            sb.AppendLine("using Glai.Allocator;");
            sb.AppendLine("using Glai.ECS;");
            sb.AppendLine("using Glai.ECS.Core;");
            sb.AppendLine("using Glai.Core;");
            foreach (string usingDirective in model.Usings)
                sb.AppendLine(usingDirective);
            sb.AppendLine();

            if (model.Namespace != null)
            {
                sb.AppendLine($"namespace {model.Namespace}");
                sb.AppendLine("{");
            }

            EmitChunkData(sb, model, indent);
            EmitBodyHelpers(sb, model, indent);

            if (!model.IsNonBurst)
            {
                switch (model.ExecutionMode)
                {
                    case QueryExecutionMode.Async:
                        EmitAsyncJob(sb, model, indent);
                        break;
                    case QueryExecutionMode.ChunkParallel:
                        EmitChunkParallelJob(sb, model, indent);
                        break;
                    case QueryExecutionMode.EntityParallel:
                        EmitEntityParallelJob(sb, model, indent);
                        break;
                }
            }

            EmitDispatch(sb, model, indent);
            EmitExtensions(sb, model, indent);

            if (model.Namespace != null)
                sb.AppendLine("}");

            spc.AddSource($"{model.SystemName}__Generated.g.cs", sb.ToString());
        }

        static void EmitChunkData(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}[StructLayout(LayoutKind.Sequential)]");
            sb.AppendLine($"{indent}internal unsafe struct {model.SystemName}__ChunkData");
            sb.AppendLine($"{indent}{{");
            for (int i = 0; i < model.Accesses.Length; i++)
            {
                sb.Append(indent).Append("    ");
                if (model.Accesses[i].Mode == AccessMode.ReadOnly)
                    sb.Append("[ReadOnly, NoAlias] ");
                else
                    sb.Append("[NoAlias] ");
                if (model.Accesses[i].IsBuffer)
                    sb.Append("public byte* c").Append(i).AppendLine(";");
                else
                    sb.Append("public ").Append(model.Accesses[i].InnerFullName).Append("* c").Append(i).AppendLine(";");
            }

            if (model.InjectEntityId)
                sb.AppendLine($"{indent}    [ReadOnly, NoAlias] public int* entityIds;");

            sb.AppendLine($"{indent}    public int entityCount;");
            if (model.ExecutionMode == QueryExecutionMode.EntityParallel)
                sb.AppendLine($"{indent}    public int entityStart;");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        static void EmitBodyHelpers(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}[BurstCompile]");
            sb.AppendLine($"{indent}internal unsafe static class {model.SystemName}__Body");
            sb.AppendLine($"{indent}{{");

            sb.AppendLine($"{indent}    [BurstCompile]");
            sb.AppendLine($"{indent}    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
            sb.AppendLine($"{indent}    internal static void ExecuteChunk(ref {model.SystemFullyQualifiedName} system,{BuildHelperParameters(model, includeCount: true)})");
            sb.AppendLine($"{indent}    {{");
            EmitExecuteChunkBody(sb, model, indent + "        ");
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        static void EmitExecuteChunkBody(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}int i = 0;");

            if (model.AvxExecute.HasValue)
                EmitSimdPath(sb, model, model.AvxExecute.Value, SimdWidth.Avx, indent);

            if (model.SseExecute.HasValue)
                EmitSimdPath(sb, model, model.SseExecute.Value, SimdWidth.Sse, indent);

            if (model.ScalarExecute.HasValue)
            {
                sb.AppendLine($"{indent}for (; i < count; i++)");
                sb.AppendLine($"{indent}{{");
                if (model.InjectEntityId)
                    sb.AppendLine($"{indent}    int entityId = entityIds[i];");
                EmitScalarBufferLocals(sb, model.ScalarExecute.Value, indent + "    ");
                EmitIndentedBody(sb, model.ScalarExecute.Value.RewrittenBody, "i", indent + "    ");
                sb.AppendLine($"{indent}}}");
                sb.AppendLine($"{indent}return;");
            }

            sb.AppendLine($"{indent}throw new global::System.InvalidOperationException(\"No supported query execute method is available for the current CPU.\");");
        }

        static void EmitSimdPath(StringBuilder sb, SystemModel model, ExecuteMethodModel execute, SimdWidth simdWidth, string indent)
        {
            int laneCount = simdWidth == SimdWidth.Avx ? 8 : 4;
            string supportCheck = simdWidth == SimdWidth.Avx
                ? "global::Unity.Burst.Intrinsics.X86.Avx2.IsAvx2Supported"
                : "global::Unity.Burst.Intrinsics.X86.Sse4_2.IsSse42Supported";
            string methodName = simdWidth == SimdWidth.Avx ? "ExecuteAVX" : "ExecuteSSE";

            sb.AppendLine($"{indent}if ({supportCheck})");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{indent}    for (; i <= count - {laneCount}; i += {laneCount})");
            sb.AppendLine($"{indent}    {{");
            EmitSimdMethodCall(sb, model, execute, simdWidth, indent + "        ", offsetExpr: "i", useTemps: false);
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine();
            sb.AppendLine($"{indent}    int remaining = count - i;");
            sb.AppendLine($"{indent}    if (remaining > 0)");
            sb.AppendLine($"{indent}    {{");
            if (execute.InjectEntityId)
            {
                sb.AppendLine($"{indent}        int* __entityIdsTemp = stackalloc int[{laneCount}];");
                sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemClear(__entityIdsTemp, {laneCount} * sizeof(int));");
                sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemCpy(__entityIdsTemp, entityIds + i, remaining * sizeof(int));");
            }
            for (int a = 0; a < execute.Accesses.Length; a++)
            {
                AccessModel access = execute.Accesses[a];
                string tempName = $"__{access.ParameterName}Temp";
                string baseName = $"__{access.ParameterName}Base";
                if (access.IsBuffer)
                {
                    string slotSize = GetBufferSlotSizeExpression(access);
                    sb.AppendLine($"{indent}        byte* {tempName} = stackalloc byte[{laneCount} * {slotSize}];");
                    sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemClear({tempName}, {laneCount} * {slotSize});");
                    sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemCpy({tempName}, {baseName} + i * {slotSize}, remaining * {slotSize});");
                }
                else
                {
                    sb.AppendLine($"{indent}        {access.InnerFullName}* {tempName} = stackalloc {access.InnerFullName}[{laneCount}];");
                    sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemClear({tempName}, {laneCount} * sizeof({access.InnerFullName}));");
                    sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemCpy({tempName}, {baseName} + i, remaining * sizeof({access.InnerFullName}));");
                }
            }
            EmitSimdMethodCall(sb, model, execute, simdWidth, indent + "        ", offsetExpr: "0", useTemps: true);
            for (int a = 0; a < execute.Accesses.Length; a++)
            {
                AccessModel access = execute.Accesses[a];
                if (access.Mode != AccessMode.ReadWrite)
                    continue;

                string tempName = $"__{access.ParameterName}Temp";
                string baseName = $"__{access.ParameterName}Base";
                if (access.IsBuffer)
                {
                    string slotSize = GetBufferSlotSizeExpression(access);
                    sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemCpy({baseName} + i * {slotSize}, {tempName}, remaining * {slotSize});");
                }
                else
                    sb.AppendLine($"{indent}        global::Unity.Collections.LowLevel.Unsafe.UnsafeUtility.MemCpy({baseName} + i, {tempName}, remaining * sizeof({access.InnerFullName}));");
            }
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}    return;");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        static void EmitSimdMethodCall(StringBuilder sb, SystemModel model, ExecuteMethodModel execute, SimdWidth simdWidth, string indent, string offsetExpr, bool useTemps)
        {
            string methodName = simdWidth == SimdWidth.Avx ? "ExecuteAVX" : "ExecuteSSE";
            int laneCount = simdWidth == SimdWidth.Avx ? 8 : 4;

            sb.Append(indent).Append("system.").Append(methodName).Append("(");
            bool needsComma = false;
            if (execute.InjectEntityId)
            {
                string entityIdsExpr = useTemps ? "__entityIdsTemp" : $"entityIds + {offsetExpr}";
                string entityIdType = simdWidth == SimdWidth.Avx ? "EntityId8" : "EntityId4";
                sb.Append("new global::Glai.ECS.").Append(entityIdType).Append("(").Append(entityIdsExpr).Append(")");
                needsComma = true;
            }

            for (int i = 0; i < execute.Accesses.Length; i++)
            {
                if (needsComma)
                    sb.Append(", ");

                AccessModel access = execute.Accesses[i];
                string wrapperName = GetSimdWrapperName(access, laneCount);
                string ptrExpr = GetSimdPointerExpression(access, offsetExpr, useTemps);
                sb.Append("new global::Glai.ECS.").Append(wrapperName).Append("<").Append(access.InnerFullName).Append(">(").Append(ptrExpr);
                if (access.IsBuffer)
                    sb.Append(", ").Append(access.BufferCapacity);
                sb.Append(")");
                needsComma = true;
            }
            sb.AppendLine(");");
        }

        static void EmitScalarBufferLocals(StringBuilder sb, ExecuteMethodModel execute, string indent)
        {
            foreach (AccessModel access in execute.Accesses)
            {
                if (!access.IsBuffer)
                    continue;

                sb.AppendLine($"{indent}var {access.ParameterName} = new global::Glai.ECS.{access.BufferWrapperName}<{access.InnerFullName}>(__{access.ParameterName}Base + i * {GetBufferSlotSizeExpression(access)}, {access.BufferCapacity});");
            }
        }

        static string GetSimdWrapperName(AccessModel access, int laneCount)
        {
            if (access.IsBuffer)
                return access.BufferWrapperName;

            return access.Mode == AccessMode.ReadOnly ? $"RefR{laneCount}" : $"RefRW{laneCount}";
        }

        static string GetSimdPointerExpression(AccessModel access, string offsetExpr, bool useTemps)
        {
            if (useTemps)
                return $"__{access.ParameterName}Temp";

            if (access.IsBuffer)
                return $"__{access.ParameterName}Base + {offsetExpr} * {GetBufferSlotSizeExpression(access)}";

            return $"__{access.ParameterName}Base + {offsetExpr}";
        }

        static string GetBufferSlotSizeExpression(AccessModel access)
            => $"(sizeof({access.InnerFullName}) * {access.BufferCapacity} + sizeof(int))";

        static string BuildHelperParameters(SystemModel model, bool includeCount)
        {
            var parts = new List<string>(model.Accesses.Length + 2);
            for (int i = 0; i < model.Accesses.Length; i++)
            {
                string attribute = model.Accesses[i].Mode == AccessMode.ReadOnly ? "[ReadOnly, NoAlias] " : "[NoAlias] ";
                string pointerType = model.Accesses[i].IsBuffer ? "byte" : model.Accesses[i].InnerFullName;
                parts.Add($"{attribute}{pointerType}* __{model.Accesses[i].ParameterName}Base");
            }

            if (model.InjectEntityId)
                parts.Add("[ReadOnly, NoAlias] int* entityIds");

            if (includeCount)
                parts.Add("int count");

            return parts.Count == 0 ? string.Empty : " " + string.Join(", ", parts);
        }

        static void EmitAsyncJob(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}[BurstCompile]");
            sb.AppendLine($"{indent}internal unsafe struct {model.SystemName}__AsyncJob : IJob");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{indent}    public {model.SystemFullyQualifiedName} System;");
            sb.AppendLine($"{indent}    [NativeDisableUnsafePtrRestriction, NoAlias] public {model.SystemName}__ChunkData* Chunks;");
            sb.AppendLine($"{indent}    public int ChunkCount;");
            sb.AppendLine();
            sb.AppendLine($"{indent}    public void Execute()");
            sb.AppendLine($"{indent}    {{");
            sb.AppendLine($"{indent}        var system = System;");
            sb.AppendLine($"{indent}        for (int chunkIndex = 0; chunkIndex < ChunkCount; chunkIndex++)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            ref var data = ref Chunks[chunkIndex];");
            EmitChunkHelperCall(sb, model, indent + "            ", "data");
            sb.AppendLine($"{indent}        }}");
            sb.AppendLine($"{indent}        System = system;");
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        static void EmitChunkParallelJob(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}[BurstCompile]");
            sb.AppendLine($"{indent}internal unsafe struct {model.SystemName}__ChunkParallelJob : IJobParallelFor");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{indent}    public {model.SystemFullyQualifiedName} System;");
            sb.AppendLine($"{indent}    [NativeDisableUnsafePtrRestriction, NoAlias] public {model.SystemName}__ChunkData* Chunks;");
            sb.AppendLine();
            sb.AppendLine($"{indent}    public void Execute(int index)");
            sb.AppendLine($"{indent}    {{");
            sb.AppendLine($"{indent}        var system = System;");
            sb.AppendLine($"{indent}        ref var data = ref Chunks[index];");
            EmitChunkHelperCall(sb, model, indent + "        ", "data");
            sb.AppendLine($"{indent}        System = system;");
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        static void EmitEntityParallelJob(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}[BurstCompile]");
            sb.AppendLine($"{indent}internal unsafe struct {model.SystemName}__EntityParallelJob : IJobParallelFor");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{indent}    public {model.SystemFullyQualifiedName} System;");
            sb.AppendLine($"{indent}    [NativeDisableUnsafePtrRestriction, NoAlias] public {model.SystemName}__ChunkData* Chunks;");
            sb.AppendLine($"{indent}    public int ChunkCount;");
            sb.AppendLine($"{indent}    public int BatchSize;");
            sb.AppendLine($"{indent}    public int TotalEntityCount;");
            sb.AppendLine();
            sb.AppendLine($"{indent}    public void Execute(int batchIndex)");
            sb.AppendLine($"{indent}    {{");
            sb.AppendLine($"{indent}        int startIndex = batchIndex * BatchSize;");
            sb.AppendLine($"{indent}        if (startIndex >= TotalEntityCount)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            return;");
            sb.AppendLine($"{indent}        }}");
            sb.AppendLine($"{indent}        int count = TotalEntityCount - startIndex;");
            sb.AppendLine($"{indent}        if (count > BatchSize)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            count = BatchSize;");
            sb.AppendLine($"{indent}        }}");
            sb.AppendLine($"{indent}        if (count <= 0 || ChunkCount <= 0)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            return;");
            sb.AppendLine($"{indent}        }}");
            sb.AppendLine();
            sb.AppendLine($"{indent}        var system = System;");
            sb.AppendLine($"{indent}        int endIndexExclusive = startIndex + count;");
            sb.AppendLine($"{indent}        int low = 0;");
            sb.AppendLine($"{indent}        int high = ChunkCount - 1;");
            sb.AppendLine($"{indent}        int chunkIndex = 0;");
            sb.AppendLine($"{indent}        while (low <= high)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            int mid = (low + high) >> 1;");
            sb.AppendLine($"{indent}            ref var midData = ref Chunks[mid];");
            sb.AppendLine($"{indent}            int midEnd = midData.entityStart + midData.entityCount;");
            sb.AppendLine($"{indent}            if (startIndex < midData.entityStart)");
            sb.AppendLine($"{indent}            {{");
            sb.AppendLine($"{indent}                high = mid - 1;");
            sb.AppendLine($"{indent}                continue;");
            sb.AppendLine($"{indent}            }}");
            sb.AppendLine($"{indent}            if (startIndex >= midEnd)");
            sb.AppendLine($"{indent}            {{");
            sb.AppendLine($"{indent}                low = mid + 1;");
            sb.AppendLine($"{indent}                continue;");
            sb.AppendLine($"{indent}            }}");
            sb.AppendLine($"{indent}            chunkIndex = mid;");
            sb.AppendLine($"{indent}            break;");
            sb.AppendLine($"{indent}        }}");
            sb.AppendLine();
            sb.AppendLine($"{indent}        int index = startIndex;");
            sb.AppendLine($"{indent}        while (index < endIndexExclusive && chunkIndex < ChunkCount)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            ref var data = ref Chunks[chunkIndex];");
            sb.AppendLine($"{indent}            int chunkEnd = data.entityStart + data.entityCount;");
            sb.AppendLine($"{indent}            int entityIndex = index - data.entityStart;");
            sb.AppendLine($"{indent}            int entityCount = endIndexExclusive < chunkEnd ? endIndexExclusive - index : chunkEnd - index;");
            EmitChunkRangeHelperCall(sb, model, indent + "            ", "data", "entityIndex", "entityCount");
            sb.AppendLine($"{indent}            index += entityCount;");
            sb.AppendLine($"{indent}            chunkIndex++;");
            sb.AppendLine($"{indent}        }}");
            sb.AppendLine($"{indent}        System = system;");
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        static void EmitDispatch(StringBuilder sb, SystemModel model, string indent)
        {
            sb.AppendLine($"{indent}internal unsafe struct {model.SystemName}__Dispatch : global::Glai.ECS.IQueryJobDispatch");
            sb.AppendLine($"{indent}{{");
            sb.AppendLine($"{indent}    public {model.SystemFullyQualifiedName} System;");
            sb.AppendLine();
            for (int i = 0; i < model.Accesses.Length; i++)
                sb.AppendLine($"{indent}    private int _s{i};");
            sb.AppendLine();
            sb.AppendLine($"{indent}    [NoAlias] private {model.SystemName}__ChunkData* _chunkDataPtr;");
            if (model.ExecutionMode == QueryExecutionMode.EntityParallel)
                sb.AppendLine($"{indent}    private int _totalEntityCount;");
            sb.AppendLine();
            sb.AppendLine($"{indent}    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
            sb.AppendLine($"{indent}    public void Prepare(global::Glai.Allocator.MemoryStateHandle arenaHandle, global::Glai.ECS.Core.ECSMemoryState memoryState, int maxChunks)");
            sb.AppendLine($"{indent}    {{");
            sb.AppendLine($"{indent}        var allocator = memoryState.GetAllocator(arenaHandle);");
            sb.AppendLine($"{indent}        var arrayHandle = allocator.AllocateArray<{model.SystemName}__ChunkData>(maxChunks);");
            sb.AppendLine($"{indent}        var chunkDataSpan = allocator.GetArray<{model.SystemName}__ChunkData>(arrayHandle);");
            sb.AppendLine($"{indent}        fixed ({model.SystemName}__ChunkData* chunkDataPtr = chunkDataSpan)");
            sb.AppendLine($"{indent}        {{");
            sb.AppendLine($"{indent}            _chunkDataPtr = chunkDataPtr;");
            sb.AppendLine($"{indent}        }}");
            if (model.ExecutionMode == QueryExecutionMode.EntityParallel)
                sb.AppendLine($"{indent}        _totalEntityCount = 0;");
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine();
            sb.AppendLine($"{indent}    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
            sb.AppendLine($"{indent}    public bool PrepareArchetype(ref global::Glai.ECS.Core.Archetype archetype)");
            sb.AppendLine($"{indent}    {{");
            for (int i = 0; i < model.Accesses.Length; i++)
                sb.AppendLine($"{indent}        _s{i} = archetype.GetComponentStorageIndex(global::Glai.Core.TypeId<{model.Accesses[i].InnerFullName}>.Id);");
            string failCheck = string.Join(" || ", Enumerable.Range(0, model.Accesses.Length).Select(i => $"_s{i} == -1"));
            sb.AppendLine($"{indent}        if ({failCheck}) return false;");
            sb.AppendLine($"{indent}        return true;");
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine();
            sb.AppendLine($"{indent}    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
            sb.AppendLine($"{indent}    public void CollectChunk(ref global::Glai.ECS.Core.Chunk chunk, int chunkIndex)");
            sb.AppendLine($"{indent}    {{");
            for (int i = 0; i < model.Accesses.Length; i++)
            {
                string castType = model.Accesses[i].IsBuffer ? "byte" : model.Accesses[i].InnerFullName;
                sb.AppendLine($"{indent}        _chunkDataPtr[chunkIndex].c{i} = ({castType}*)chunk.GetComponentPtr(_s{i});");
            }
            if (model.InjectEntityId)
                sb.AppendLine($"{indent}        _chunkDataPtr[chunkIndex].entityIds = chunk.GetEntityIds();");
            sb.AppendLine($"{indent}        _chunkDataPtr[chunkIndex].entityCount = chunk.EntityCount;");
            if (model.ExecutionMode == QueryExecutionMode.EntityParallel)
            {
                sb.AppendLine($"{indent}        _chunkDataPtr[chunkIndex].entityStart = _totalEntityCount;");
                sb.AppendLine($"{indent}        _totalEntityCount += chunk.EntityCount;");
            }
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine();
            sb.AppendLine($"{indent}    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
            sb.AppendLine($"{indent}    public global::Unity.Jobs.JobHandle ScheduleParallel(int chunkCount)");
            sb.AppendLine($"{indent}    {{");
            if (model.IsNonBurst && model.ExecutionMode != QueryExecutionMode.MainThread)
            {
